CREATE PROCEDURE [dbo].[PRODUCTOS_Select]
  @ID NUMERIC = NULL
 ,@PRODUCTO_ID NUMERIC = NULL
 ,@NOMBRE VARCHAR(200) = NULL
 ,@DESCRIPCION VARCHAR(4000) = NULL
 ,@CATEGORIA VARCHAR(4000) = NULL
 ,@PRECIO_LISTA NUMERIC = NULL
 ,@FABRICANTE VARCHAR(4000) = NULL
 ,@IMAGEN VARBINARY = NULL
 ,@IMAGEN_URL VARCHAR(4000) = NULL
AS
 SET NOCOUNT OFF;

    SELECT ID
    ,PRODUCTO_ID
    ,NOMBRE
    ,DESCRIPCION
    ,CATEGORIA
    ,PRECIO_LISTA
    ,FABRICANTE
    ,IMAGEN
    ,IMAGEN_URL
    FROM PRODUCTOS
    WHERE (@ID IS NULL OR @ID=ID)
   AND (@PRODUCTO_ID IS NULL OR @PRODUCTO_ID=PRODUCTO_ID)
   AND (@NOMBRE IS NULL OR @NOMBRE=NOMBRE)
   AND (@DESCRIPCION IS NULL OR @DESCRIPCION=DESCRIPCION)
   AND (@CATEGORIA IS NULL OR @CATEGORIA=CATEGORIA)
   AND (@PRECIO_LISTA IS NULL OR @PRECIO_LISTA=PRECIO_LISTA)
   AND (@FABRICANTE IS NULL OR @FABRICANTE=FABRICANTE)
   AND (@IMAGEN IS NULL OR @IMAGEN=IMAGEN)
   AND (@IMAGEN_URL IS NULL OR @IMAGEN_URL=IMAGEN_URL)

-- =====EXISTS==================================
SET ANSI_NULLS ON

GO


